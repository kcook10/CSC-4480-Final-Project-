  Lname  Char(50)  NOT NULL,
  Fname  Char(50)  NOT NULL,
  PhoneNum  Char(12)  NOT NULL,
  CustomerID  Int  NOT NULL,
  CONSTRAINT  Customer_PK  PRIMARY KEY(CustomerID)
  );

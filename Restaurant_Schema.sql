CREATE TABLE EMPLOYEES(
  Lname  Char(50)  NOT NULL,
  Fname  Char(50)  NOT NULL,
  PhoneNum  Char(12)  NOT NULL,
  HoursWorked  Int  NOT NULL,
  EmployeeID  Int  NOT NULL,
  CONSTRAINT  EMPLOYEES_PK  PRIMARY KEY(EmployeeID)
  );
